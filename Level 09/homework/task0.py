# შესთავაზეთ მომხმარებელს შეიყვანოს პირველი პირის ასაკი
age1 = int(input("შეიყვანეთ პირველი პირის ასაკი: "))

# სთხოვებით მომხმარებელს შეიყვანოს მეორე პირის ასაკი
age2 = int(input("შეიყვანეთ მეორე პირის ასაკი: "))

# შეადარეთ ორი ასაკი შედარების ოპერატორების გამოყენებით
if age1 > age2:
    print("პირველი პირი უფროსია.")
elif age2 > age1:
    print("მეორე პირი უფროსია.")
else:
    print("ორივე პირი არის იგივე ასაკი.")